# Python program to display the multiplication table using for loop

num = int (input( " Enter a Number: "))

for i in range(1, 11):
    print(f"{num} x {i} =",num*i)



# Python program to display the multiplication table using while loop
 
n = 7
i = 1

while i <= 10:
    print(n, "x", i, "=", n*i)

    i+=1
    

